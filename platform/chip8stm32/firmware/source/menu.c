static uint8_t buffer[1024];


static inline bool getPixel(int row, int col)
{
    if (row >= 0 && row <= 63 && col >= 0 && col <= 127)
        return (buffer[(128 * row + col) / 8] >> (7 - (col % 8))) & 1;
    else
        return 0;
}


static inline void setPixel(int row, int col)
{
    if (row >= 0 && row <= 63 && col >= 0 && col <= 127)
        buffer[(128 * row + col) / 8] |= (1 << (7 - (col % 8)));
}


static inline void clearPixel(int row, int col)
{
    if (row >= 0 && row <= 63 && col >= 0 && col <= 127)
        buffer[(128 * row + col) / 8] &= ~(1 << (7 - (col % 8)));
}


